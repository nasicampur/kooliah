package Java_STMIK;

public class Tugas_pert5_while {
    
    public static void main(String[] args) {
        System.out.println("===Perulangan while===");
        int ulang = 5;
        while(ulang > 0){
            System.out.print(ulang+",");
            ulang--;
        }
        System.out.print('.');
    }
}