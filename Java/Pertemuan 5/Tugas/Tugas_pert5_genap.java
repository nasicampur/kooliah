package Java_STMIK;

public class Tugas_pert5_genap {
    
    public static void main(String[] args) {
        System.out.println("=====Perulangan while bil genap=====");
        int bil = 2;
        while(bil <= 20){
            System.out.println("Bil genap : "+bil);
            bil = bil + 2;
        }
        System.out.println("====================================");
    }
}
