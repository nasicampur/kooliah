package Pertemuan4;

public class switch1 {
    public static void main(String[] args) {
        int x=1;
        switch(x){
            case 1:
                System.out.println("X = 1");
                break;
            case 2:
                System.out.println("X = 2");
                break;
            case 3:
                System.out.println("X = 3");
                break;
        }
    }
}
