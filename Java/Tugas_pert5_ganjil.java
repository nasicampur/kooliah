package Java_STMIK;

public class Tugas_pert5_ganjil {

    public static void main(String[] args) {
        System.out.println("=====Perulangan while bil ganjil=====");
        int bil = 1;
        while(bil <= 10){
            System.out.println("Bilangan ganjil : "+bil);
            bil = bil+2;
        }
        System.out.println("======================================");
    }
}
